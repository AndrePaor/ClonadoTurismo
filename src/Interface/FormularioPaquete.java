Traslado traslado = new Traslado (Integer.parseInt(tfIdtraslado.getText())) ;
Alojamiento alojamiento = new Alojamiento (Integer.parseInt(tfIdalojamiento.getText())) ;

paquete.setTraslado(traslado);
paquete.setAlojamiento(alojamiento);
paquete.setDescripcion(tfdescripcion.getText());
    
paquete.setCupo(paquete.cupo());

paqueteData.guardarPaquete(paquete);
tfId.setText(Integer.toString(paquete.getId_paquete()));
tfcupo.setText(Integer.toString(paquete.getCupo()));
